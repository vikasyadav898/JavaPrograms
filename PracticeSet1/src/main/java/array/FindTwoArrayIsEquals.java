package array;

import java.util.Arrays;

public class FindTwoArrayIsEquals {

	public static void main(String[] args) {
		int arr1[]= {10,20,30};
		int arr2[]= {10,20,30};
		if(Arrays.equals(arr1, arr2))
		{
			System.out.println("Both are equals");
		}
		else
		{
			System.out.println("Both are not equals");
		}

	}

}
